// Excel y√ºkle - Mesai ve devamsƒ±zlƒ±k toplu kayƒ±t
router.post('/excel/upload', upload.single('file'), async (req, res) => {
  try {
    if (!req.hrYetkileri.puanlamaYapabilir) {
      return res.status(403).json({ message: 'Excel y√ºkleme yetkiniz yok' });
    }

    if (!req.file) {
      return res.status(400).json({ message: 'Dosya y√ºklenmedi' });
    }

    console.log('üìä Excel Upload Request:', {
      userId: req.user?.id,
      fileName: req.file.originalname,
      fileSize: req.file.size,
    });

    // Excel dosyasƒ±nƒ± oku
    const data = await ExcelService.parseExcelFile(req.file.buffer);

    if (!data || data.length === 0) {
      return res
        .status(400)
        .json({ message: 'Excel dosyasƒ±nda veri bulunamadƒ±' });
    }

    console.log('üìä Excel Parse Debug:', {
      totalRows: data.length,
      firstRowKeys: data[0] ? Object.keys(data[0]) : 'Yok',
      firstRowData: data[0] || 'Yok',
      secondRowData: data[1] || 'Yok',
    });

    // Ayarlarƒ± getir
    const settings = await HRSettings.getSettings();

    const results = {
      basarili: 0,
      hatali: 0,
      hatalar: [],
      islenmeyen: 0,
    };

    // Her satƒ±rƒ± i≈üle
    for (let i = 0; i < data.length; i++) {
      const row = data[i];
      const rowNumber = i + 2; // Excel'de ba≈ülƒ±k satƒ±rƒ± var

      try {
        // Kullanƒ±cƒ± adƒ± kontrol√º (yeni format)
        const kullaniciAdi = row['Kullanƒ±cƒ± Adƒ±']?.toString().trim();
        if (!kullaniciAdi) {
          results.islenmeyen++;
          continue; // Bo≈ü satƒ±rlarƒ± atla
        }

        // Kullanƒ±cƒ±yƒ± kullanƒ±cƒ± adƒ±na g√∂re bul
        const user = await User.findOne({ kullaniciAdi: kullaniciAdi });
        if (!user) {
          results.hatalar.push({
            satir: rowNumber,
            kullanici: kullaniciAdi,
            hata: 'Kullanƒ±cƒ± bulunamadƒ±',
          });
          results.hatali++;
          continue;
        }

        // Tarih kontrol√º (yeni format)
        const tarihText = row['Tarih (Ay/Yƒ±l)']?.toString().trim();
        if (!tarihText) {
          results.hatalar.push({
            satir: rowNumber,
            kullanici: kullaniciAdi,
            hata: 'Tarih bilgisi eksik',
          });
          results.hatali++;
          continue;
        }

        // Tarih parse i≈ülemi
        let parsedDate;
        try {
          // "01/2025" veya "Ocak 2025" formatlarƒ±nƒ± destekle
          if (tarihText.includes('/')) {
            const [ayStr, yilStr] = tarihText.split('/');
            parsedDate = {
              ay: parseInt(ayStr),
              yil: parseInt(yilStr),
            };
          } else {
            // "Ocak 2025" formatƒ±
            const ayAdlari = {
              ocak: 1,
              ≈üubat: 2,
              mart: 3,
              nisan: 4,
              mayƒ±s: 5,
              haziran: 6,
              temmuz: 7,
              aƒüustos: 8,
              eyl√ºl: 9,
              ekim: 10,
              kasƒ±m: 11,
              aralƒ±k: 12,
            };

            const parts = tarihText.toLowerCase().split(' ');
            if (parts.length !== 2) {
              throw new Error('Ge√ßersiz tarih formatƒ±');
            }

            const ayAdi = parts[0];
            const yil = parseInt(parts[1]);

            if (!ayAdlari[ayAdi] || isNaN(yil)) {
              throw new Error('Ge√ßersiz tarih formatƒ±');
            }

            parsedDate = {
              ay: ayAdlari[ayAdi],
              yil: yil,
            };
          }
        } catch {
          results.hatalar.push({
            satir: rowNumber,
            kullanici: kullaniciAdi,
            hata: `Ge√ßersiz tarih formatƒ±: ${tarihText}. Kullanƒ±n: "01/2025" veya "Ocak 2025"`,
          });
          results.hatali++;
          continue;
        }

        // Miktar deƒüerlerini al (yeni format)
        const devamsizlikSaat = parseFloat(row['Devamsƒ±zlƒ±k (Saat)']) || 0;
        const fazlaMesai = parseFloat(row['Fazla Mesai (Saat)']) || 0;

        // Hi√ß veri yoksa atla
        if (fazlaMesai === 0 && devamsizlikSaat === 0) {
          results.islenmeyen++;
          continue;
        }

        // HRScore kaydƒ±nƒ± bul veya olu≈ütur
        let hrScore = await HRScore.findOne({
          kullanici: user._id,
          'donem.yil': parsedDate.yil,
          'donem.ay': parsedDate.ay,
        });

        if (!hrScore) {
          hrScore = new HRScore({
            kullanici: user._id,
            donem: {
              yil: parsedDate.yil,
              ay: parsedDate.ay,
            },
          });
        }

        // Mevcut aylƒ±k kayƒ±tlarƒ± kontrol et ve temizle
        const entryDate = new Date(parsedDate.yil, parsedDate.ay - 1, 1);

        // Fazla mesai ekle
        if (fazlaMesai > 0) {
          // Mevcut aylƒ±k mesai kaydƒ±nƒ± temizle
          hrScore.mesaiKayitlari = hrScore.mesaiKayitlari.filter(
            m =>
              !(
                m.tarih.getMonth() === entryDate.getMonth() &&
                m.tarih.getFullYear() === entryDate.getFullYear() &&
                m.aciklama &&
                m.aciklama.includes('AYLIK_TOPLAM')
              ),
          );

          const mesaiPuani = fazlaMesai * settings.mesaiPuanlama.saatBasinaPuan;
          hrScore.mesaiKayitlari.push({
            tarih: entryDate,
            saat: fazlaMesai,
            puan: mesaiPuani,
            aciklama: `AYLIK_TOPLAM: ${fazlaMesai} saat fazla mesai (${parsedDate.ay}/${parsedDate.yil}) - Excel ile y√ºklendi`,
            olusturanKullanici: req.user.id,
          });
        }

        // Devamsƒ±zlƒ±k (saat) ekle
        if (devamsizlikSaat > 0) {
          // Mevcut aylƒ±k saat bazlƒ± devamsƒ±zlƒ±k kaydƒ±nƒ± temizle
          hrScore.devamsizlikKayitlari = hrScore.devamsizlikKayitlari.filter(
            d =>
              !(
                d.tarih.getMonth() === entryDate.getMonth() &&
                d.tarih.getFullYear() === entryDate.getFullYear() &&
                d.tur === 'saat' &&
                d.aciklama &&
                d.aciklama.includes('AYLIK_TOPLAM')
              ),
          );

          const devamsizlikPuani =
            devamsizlikSaat * settings.devamsizlikPuanlama.saatBasinaPuan;
          hrScore.devamsizlikKayitlari.push({
            tarih: entryDate,
            tur: 'saat',
            miktar: devamsizlikSaat,
            puan: devamsizlikPuani,
            aciklama: `AYLIK_TOPLAM: ${devamsizlikSaat} saat devamsƒ±zlƒ±k (${parsedDate.ay}/${parsedDate.yil}) - Excel ile y√ºklendi`,
            olusturanKullanici: req.user.id,
          });
        }

        await hrScore.save();
        results.basarili++;

        console.log(
          `‚úÖ Row ${rowNumber} processed: ${user.ad} ${user.soyad} (${kullaniciAdi}) - Tarih: ${parsedDate.ay}/${parsedDate.yil}, Mesai: ${fazlaMesai}s, Devamsƒ±zlƒ±k: ${devamsizlikSaat}s`,
        );
      } catch (error) {
        console.error(`Row ${rowNumber} error:`, error);
        results.hatalar.push({
          satir: rowNumber,
          kullanici: row['Kullanƒ±cƒ± Adƒ±']?.toString() || 'Bilinmeyen',
          hata: error.message,
        });
        results.hatali++;
      }
    }

    console.log('üìä Excel Upload Results:', {
      totalRows: data.length,
      basarili: results.basarili,
      hatali: results.hatali,
      islenmeyen: results.islenmeyen,
      errorCount: results.hatalar.length,
    });

    res.json({
      message: `Excel y√ºkleme tamamlandƒ±. ${results.basarili} kayƒ±t ba≈üarƒ±lƒ±, ${results.hatali} kayƒ±t hatalƒ±, ${results.islenmeyen} kayƒ±t i≈ülenmedi.`,
      results,
    });
  } catch (error) {
    console.error('Excel upload error:', error);
    res.status(500).json({
      message: 'Excel y√ºkleme sƒ±rasƒ±nda hata olu≈ütu: ' + error.message,
    });
  }
});

