// HR Evaluation endpoint
router.post('/evaluations', async (req, res) => {
  try {
    const { templateId, userId, maddeler, toplamPuan, maksimumPuan, notlar } =
      req.body;

    if (!templateId || !userId || !maddeler) {
      return res.status(400).json({ message: 'Gerekli alanlar eksik' });
    }

    // HRScore modeli ile deƒüerlendirme kaydet
    const currentDate = new Date();
    const donem = {
      yil: currentDate.getFullYear(),
      ay: currentDate.getMonth() + 1,
    };

    // Kullanƒ±cƒ±nƒ±n bu d√∂nem i√ßin HR score'u var mƒ± kontrol et
    let hrScore = await HRScore.findOne({
      kullanici: userId,
      'donem.yil': donem.yil,
      'donem.ay': donem.ay,
    });

    if (!hrScore) {
      // Yeni HR score olu≈ütur
      hrScore = new HRScore({
        kullanici: userId,
        donem: donem,
        checklistPuanlari: [],
        mesaiKayitlari: [],
        devamsizlikKayitlari: [],
        toplamPuan: 0,
      });
    }

    // Yeni deƒüerlendirme ekle
    const yeniDegerlendirme = {
      sablon: templateId,
      tarih: new Date(),
      madde: {
        puan: toplamPuan,
        maksimumPuan: maksimumPuan,
        detaylar: maddeler,
      },
      notlar: notlar || '',
      degerlendiren: req.user.id,
    };

    hrScore.checklistPuanlari.push(yeniDegerlendirme);

    // Toplam puanƒ± g√ºncelle
    hrScore.toplamPuan =
      hrScore.checklistPuanlari.reduce(
        (total, cp) => total + (cp.madde.puan || 0),
        0,
      ) +
      hrScore.mesaiKayitlari.reduce((total, mk) => total + (mk.puan || 0), 0) +
      hrScore.devamsizlikKayitlari.reduce(
        (total, dk) => total + (dk.puan || 0),
        0,
      );

    await hrScore.save();

    res.json({
      message: 'Deƒüerlendirme ba≈üarƒ±yla kaydedildi',
      hrScore: hrScore,
    });
  } catch (error) {
    console.error('HR evaluation error:', error);
    res
      .status(500)
      .json({ message: 'Deƒüerlendirme kaydedilirken hata olu≈ütu' });
  }
});

