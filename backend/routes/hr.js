router.post('/manual-entry', async (req, res) => {
  try {
    if (!req.hrYetkileri.puanlamaYapabilir) {
      return res.status(403).json({ message: 'Puanlama yapma yetkiniz yok' });
    }

    const {
      kullaniciId,
      tip,
      yil,
      ay,
      tarih,
      miktar,
      aciklama,
      aylikkGirisTipi,
    } = req.body;

    console.log('üìù Manual Entry Request:', {
      kullaniciId,
      tip,
      yil,
      ay,
      tarih,
      miktar,
      aciklama,
      aylikkGirisTipi,
    });

    // Aylƒ±k giri≈ü sistemi i√ßin validasyon
    if (aylikkGirisTipi) {
      if (!kullaniciId || !tip || !yil || !ay || !miktar) {
        return res.status(400).json({
          message:
            'Aylƒ±k giri≈ü i√ßin gerekli alanlar: kullaniciId, tip, yil, ay, miktar',
        });
      }
    } else {
      // G√ºnl√ºk giri≈ü (eski sistem) i√ßin validasyon
      if (!kullaniciId || !tip || !tarih || !miktar) {
        return res.status(400).json({
          message:
            'G√ºnl√ºk giri≈ü i√ßin gerekli alanlar: kullaniciId, tip, tarih, miktar',
        });
      }
    }

    // Ayarlarƒ± getir
    const settings = await HRSettings.getSettings();

    let donem;
    let entryDate;

    if (aylikkGirisTipi) {
      // Aylƒ±k giri≈ü sistemi
      donem = {
        yil: parseInt(yil),
        ay: parseInt(ay),
      };
      // Aylƒ±k kayƒ±t i√ßin ayƒ±n ilk g√ºn√ºn√º kullan
      entryDate = new Date(yil, ay - 1, 1);
    } else {
      // G√ºnl√ºk giri≈ü (eski sistem)
      entryDate = new Date(tarih);
      donem = {
        yil: entryDate.getFullYear(),
        ay: entryDate.getMonth() + 1,
      };
    }

    // Mevcut d√∂nem kaydƒ±nƒ± bul veya olu≈ütur
    let hrScore = await HRScore.findOne({
      kullanici: kullaniciId,
      'donem.yil': donem.yil,
      'donem.ay': donem.ay,
    });

    if (!hrScore) {
      hrScore = new HRScore({
        kullanici: kullaniciId,
        donem: donem,
      });
    }

    // Aylƒ±k giri≈ü i√ßin aynƒ± tip kaydƒ±n olup olmadƒ±ƒüƒ±nƒ± kontrol et
    if (aylikkGirisTipi) {
      if (tip === 'mesai') {
        // Mevcut aylƒ±k mesai kaydƒ± var mƒ± kontrol et
        const existingMesai = hrScore.mesaiKayitlari.find(
          m =>
            m.tarih.getMonth() === entryDate.getMonth() &&
            m.tarih.getFullYear() === entryDate.getFullYear() &&
            m.aciklama &&
            m.aciklama.includes('AYLIK_TOPLAM'),
        );

        if (existingMesai) {
          return res.status(400).json({
            message: `${donem.ay}/${donem.yil} d√∂nemi i√ßin zaten aylƒ±k mesai kaydƒ± bulunmaktadƒ±r. √ñnce mevcut kaydƒ± silmeniz gerekiyor.`,
          });
        }
      } else if (tip === 'devamsizlik_gun' || tip === 'devamsizlik_saat') {
        // Mevcut aylƒ±k devamsƒ±zlƒ±k kaydƒ± var mƒ± kontrol et
        const existingDevamsizlik = hrScore.devamsizlikKayitlari.find(
          d =>
            d.tarih.getMonth() === entryDate.getMonth() &&
            d.tarih.getFullYear() === entryDate.getFullYear() &&
            d.aciklama &&
            d.aciklama.includes('AYLIK_TOPLAM') &&
            ((tip === 'devamsizlik_gun' && d.tur === 'tam_gun') ||
              (tip === 'devamsizlik_saat' && d.tur === 'saat')),
        );

        if (existingDevamsizlik) {
          const tipAdi =
            tip === 'devamsizlik_gun'
              ? 'g√ºn bazlƒ± devamsƒ±zlƒ±k'
              : 'saat bazlƒ± devamsƒ±zlƒ±k';
          return res.status(400).json({
            message: `${donem.ay}/${donem.yil} d√∂nemi i√ßin zaten aylƒ±k ${tipAdi} kaydƒ± bulunmaktadƒ±r. √ñnce mevcut kaydƒ± silmeniz gerekiyor.`,
          });
        }
      }
    }

    // Kayƒ±t ekle
    if (tip === 'mesai') {
      const mesaiPuani = miktar * settings.mesaiPuanlama.saatBasinaPuan;
      const aciklamaText = aylikkGirisTipi
        ? `AYLIK_TOPLAM: ${miktar} saat fazla mesai (${donem.ay}/${donem.yil}) ${aciklama ? '- ' + aciklama : ''}`
        : aciklama || '';

      hrScore.mesaiKayitlari.push({
        tarih: entryDate,
        saat: miktar,
        puan: mesaiPuani,
        aciklama: aciklamaText,
        olusturanKullanici: req.user.id,
      });
    } else if (tip === 'devamsizlik_gun') {
      // Devamsƒ±zlƒ±k kaydƒ± ekle
      const devamsizlikPuani =
        miktar * settings.devamsizlikPuanlama.gunBasinaPuan;
      const aciklamaText = aylikkGirisTipi
        ? `AYLIK_TOPLAM: ${miktar} g√ºn devamsƒ±zlƒ±k (${donem.ay}/${donem.yil}) ${aciklama ? '- ' + aciklama : ''}`
        : aciklama || '';

      hrScore.devamsizlikKayitlari.push({
        tarih: entryDate,
        tur: 'tam_gun',
        miktar: miktar,
        puan: devamsizlikPuani,
        aciklama: aciklamaText,
        olusturanKullanici: req.user.id,
      });
    } else if (tip === 'devamsizlik_saat') {
      const devamsizlikPuani =
        miktar * settings.devamsizlikPuanlama.saatBasinaPuan;
      const aciklamaText = aylikkGirisTipi
        ? `AYLIK_TOPLAM: ${miktar} saat devamsƒ±zlƒ±k (${donem.ay}/${donem.yil}) ${aciklama ? '- ' + aciklama : ''}`
        : aciklama || '';

      hrScore.devamsizlikKayitlari.push({
        tarih: entryDate,
        tur: 'saat',
        miktar: miktar,
        puan: devamsizlikPuani,
        aciklama: aciklamaText,
        olusturanKullanici: req.user.id,
      });
    } else {
      return res.status(400).json({ message: 'Ge√ßersiz tip' });
    }

    await hrScore.save();
    await hrScore.populate('kullanici', 'ad soyad');

    console.log('‚úÖ Manual Entry Saved:', {
      scoreId: hrScore._id,
      kullanici: hrScore.kullanici.ad + ' ' + hrScore.kullanici.soyad,
      tip,
      miktar,
      donem,
      aylikkGirisTipi,
    });

    const successMessage = aylikkGirisTipi
      ? `${donem.ay}/${donem.yil} d√∂nemi aylƒ±k kaydƒ± ba≈üarƒ±yla eklendi`
      : 'Kayƒ±t ba≈üarƒ±yla eklendi';

    res.json({
      message: successMessage,
      hrScore,
    });
  } catch (error) {
    console.error('Manual entry error:', error);
    res.status(500).json({ message: 'Sunucu hatasƒ±' });
  }
});

